#include "Parser.hpp"
/***********************************************************************//**
	@brief 
***************************************************************************/
int main(int argc, const char** argv) {
  Parser parser;
  std::string line;
  while(std::getline(std::cin, line)) {
    std::cout << ">" << line << std::endl;
    std::any result = parser.parse(line);
    std::cout << "=> " << std::any_cast<int>(result) << std::endl;
  }
  return 0;
}
